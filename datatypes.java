public class datatypes{
    public static void main(String args[]){
        int i=10;
        char ch='a';
        float f=2.5f;
        double d=2.3445d;
        boolean b = true;
        System.out.println("integer: " + i);
        System.out.println("character: " + ch);
        System.out.println("float: " + f);
        System.out.println("double: " + d);
        System.out.println("boolean: " + b);
    }
}